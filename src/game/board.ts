export function createPropertyStates(): Record<string, PropertyState> {
  const states: Record<string, PropertyState> = {}
  for (const space of BOARD_SPACES) {
    if (
      space.type === 'property' ||
      space.type === 'railroad' ||
      space.type === 'utility'
    ) {
      states[space.id] = { ownerId: null, houses: 0, isMortgaged: false }
    }
  }
  return states
}
